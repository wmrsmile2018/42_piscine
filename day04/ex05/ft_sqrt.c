int		ft_sqrt(int nb)
{
	int i;
	int i2;

	if (nb < 0)
		return (0);
	i = 1;
	i2 = i * i;
	while (i2 < nb && i < 46340)
	{
		i++;
		i2 = i * i;
	}
	if (i2 != nb)
		return (0);
	return (i);
}
