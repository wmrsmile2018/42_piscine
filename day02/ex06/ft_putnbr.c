int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int len;
	int b;

	if (nb < 0)
	{
		nb *= -1;
		ft_putchar('-');
	}
	b = 1;
	while (nb / b > 9)
	{
		b *= 10;
	}
	while (b >= 1)
	{
		ft_putchar('0' + nb / b);
		nb %= b;
		b /= 10;
	}
	ft_putchar('\n');
}
